public class OddEvenNumber
{
  public static void main(String [] args)
   {
   int n= 5 , m=20;
   double res=(double)n+m;
   System.out.println("Result =   "  + res);
   if (res%2==0)
    {
	 System.out.println("Result number is Even.");
	}
	else
	{
	  System.out.println("Result number is ODD.");
	}
   }
}